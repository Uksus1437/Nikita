def dist(a, b):
    x1, y1 = a[0], a[1]
    x2, y2 = b[0], b[1]
    s = ((x2 - x1) ** 2 + (y2 - y1) ** 2) ** 0.5
    return s


f = open("27.txt").readlines()
aq = [d.replace(",", ".") for d in f]
q = [d.split() for d in aq]
fi = [list(map(float, d)) for d in q]
sm = []

center1 = []
center2 = []
center3 = []

for point in fi:
    x, y = point[0], point[1]
    
    if (-10 < y < 10) and (0 < x < 20):
        center1.append(point)
    elif 10 < y < 30 and 5 < x < 25:
        center2.append(point)
    elif 0 > y > -30 and 20 < x < 40:
        center3.append(point)

print(center1)
for c1 in center1:
    for c2 in center2:
        for c3 in center3:
            s = []
            for p in fi:
                sm1 = dist(c1, p)
                sm2 = dist(c2, p)
                sm3 = dist(c3, p)
                s.append(min(sm1, sm2, sm3))
            sm.append([sum(s), c1, c2, c3])

res = min(sm)

print(res)
px = ((res[1][0] + res[2][0] + res[3][0]) / 3) * 10000
yx = ((res[1][1] + res[2][1] + res[3][0]) / 3) * 10000
